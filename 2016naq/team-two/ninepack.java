import java.util.Scanner;


public class ninepack {

	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		int numBuns = scan.nextInt();
		int[] buns = new int[numBuns];
		for (int i = 0; i < numBuns; i++) {
			buns[i] = scan.nextInt();
		}
		int numDogs = scan.nextInt();
		int[] dogs = new int[numDogs];
		for (int i = 0; i < numDogs; i++) {
			dogs[i] = scan.nextInt();
		}
		
		
		
	}

}
